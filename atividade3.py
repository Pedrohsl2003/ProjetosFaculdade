#3

anos = int(input("Digite sua idade em anos: "))
meses = int(input("Digite o número de meses adicionais: "))
dias = int(input("Digite o número de dias adicionais: "))

idade_dias = anos * 365 + meses * 30 + dias

print('A idade em dias é:', idade_dias, 'dias')

