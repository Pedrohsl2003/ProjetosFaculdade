#Atividade ↓

import math

#1

h = float(input('Digite a altura do tronco da pirâmide: '))
Bmenor = float(input('Digite o tamanho da base menor da pirâmide: '))
Bmaior = float(input('Digite o tamanho da base maior da pirâmide: '))

volume = (h / 3) * (Bmaior ** 2 + Bmenor ** 2 + Bmenor * Bmaior)

print('O volume do tronco da pirâmide será de: {:.2f}'.format(volume),'cm³')

